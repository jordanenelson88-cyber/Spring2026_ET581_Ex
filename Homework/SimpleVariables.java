public class SimpleVariables{
    public static void main(String[] args){
       // this script will store my info
       // int stores whole numbers
        int age = 19; // int age will store my age(which is a whole number)
        double height = 6.0; //double height will store my height(which is a decimal)
        char grade = 'T'; // char grade will store the letter

     System.out.println("My age is: " + age);
     System.out.println("My height is: " + height);
     System.out.println("My grade is: " + grade);
    }

}